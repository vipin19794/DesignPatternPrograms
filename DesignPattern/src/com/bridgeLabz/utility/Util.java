package com.bridgeLabz.utility;

import java.util.Scanner;

public class Util 
{
      private static Scanner sc = new Scanner(System.in);
      
      public static String next()
      {
		return sc.next();  
      }
      
      public static String nextLine()
      {
		return sc.nextLine();	  
      }
      
      public static int integerInt()
      {
		return sc.nextInt();	  
      }
      
      public static float integerFloat()
      {
		return sc.nextFloat();	  
      }
      
      public static double integerDouble()
      {
		return sc.nextDouble();	  
      }
}
